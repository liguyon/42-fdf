#include "fdf.h"

t_vec4	vec4_create_from_vec3(t_vec3 v)
{
	t_vec4	ret;

	ret = (t_vec4){
		.x = v.x,
		.y = v.y,
		.z = v.z,
		.w = 1
	};
	return (ret);
}
